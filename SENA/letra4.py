frase = "el carro esta muy caro y esta muy grande caro caro"

a = 0
e = 0
i = 0
o = 0
u = 0

for j in frase:

    if j == 'a':

        a += 1

    elif j == 'e':

        e += 1
    
    elif j == 'i':

        i += 1

    elif j == 'o':

        o += 1

    elif j == 'u':

        u += 1
    

print(a)
print(e)
print(i)
print(o)
print(u)